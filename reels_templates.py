def get_season(month: int) -> str:
    if month in (12, 1, 2):
        return "зима"
    if month in (3, 4, 5):
        return "весна"
    if month in (6, 7, 8):
        return "лето"
    return "осень"


def is_nesting_ban(month: int) -> bool:
    """С 1 апреля по 10 июня — нерестовый запрет на спиннинг в МО."""
    return month in (4, 5)  # июнь частично, но темы уже про открытие


def build_reels_hashtags(
    month: int,
    topic: str = "",
    count: int = 25,
) -> str:
    """
    Собирает оптимальный набор хэштегов для Instagram Reels.
    
    Стратегия (рекомендация Instagram):
    - 3-5 крупных (широкий охват, быстрые показы)
    - 5-7 средних (целевая аудитория)
    - 5-7 нишевых (точное попадание)
    - 3-5 микро (свои + без конкуренции)
    - 2-3 сезонных
    - 1-2 месячных
    - 1-2 водоёма (если в теме)
    - 1-2 видовых (если в теме)
    """
    season = get_season(month)
    tags = set()

    # Крупные: 3-4
    tags.update(random.sample(BIG_TAGS, min(4, len(BIG_TAGS))))

    # Средние: 5-6
    tags.update(random.sample(MID_TAGS, min(6, len(MID_TAGS))))

    # Нишевые: 4-5
    tags.update(random.sample(NICHE_TAGS, min(5, len(NICHE_TAGS))))

    # Микро: 2-3
    tags.update(random.sample(MICRO_TAGS, min(3, len(MICRO_TAGS))))

    # Сезонные: 2-3
    seasonal = SEASONAL_TAGS.get(season, [])
    if seasonal:
        tags.update(random.sample(seasonal, min(3, len(seasonal))))

    # Месячные: 1-2
    monthly = MONTHLY_TAGS.get(month, [])
    if monthly:
        tags.update(random.sample(monthly, min(2, len(monthly))))

    # Видовые по теме
    topic_l = topic.lower()
    for species, species_tags in SPECIES_TAGS.items():
        if species in topic_l or any(w in topic_l for w in species.split()):
            tags.update(random.sample(species_tags, min(2, len(species_tags))))
            break

    # Водоёмы по теме
    for group, wb_tags in WATERBODY_TAGS.items():
        for tag in wb_tags:
            clean = tag.replace("#", "").replace("рыбалкана", "").replace("рыбалка", "")
            if clean in topic_l:
                tags.add(tag)
                break

    # Обязательные
    tags.add("#zajabri")
    tags.add("#спиннинг")
    tags.add("#рыбалка")

    # Обрезаем до лимита
    tag_list = list(tags)[:count]
    random.shuffle(tag_list)
    return " ".join(tag_list)


def get_random_hook(month: int, topic: str = "") -> str:
    """Возвращает случайный хук для первой строки Reels."""
    if is_nesting_ban(month):
        # В нерестовый запрет — специальные хуки
        pool = HOOKS["запрет"] + HOOKS["факт"]
    else:
        # Обычные — взвешиваем: вопросы и факты чаще
        pool = HOOKS["вопрос"] * 2 + HOOKS["факт"] * 2 + HOOKS["провокация"]
    return random.choice(pool)


def get_random_cta() -> str:
    """Случайный призыв к действию."""
    return random.choice(CTA_TEMPLATES)


def build_reels_caption_template(
    month: int,
    topic: str = "",
) -> str:
    """
    Полный шаблон подписи для Reels:
    1. Хук
    2. [место для текста — заполняет GPT]
    3. CTA
    4. Хэштеги
    """
    hook = get_random_hook(month, topic)
    cta = get_random_cta()
    hashtags = build_reels_hashtags(month, topic)

    template = (
        f"{hook}\n"
        f"\n"
        f"[ТЕКСТ О: {topic}]\n"
        f"\n"
        f"{cta}\n"
        f"\n"
        f".\n.\n.\n"
        f"{hashtags}"
    )
    return template


