async def search_pexels_video(query: str) -> Optional[str]:
    """Ищет видео на Pexels, возвращает URL mp4 файла."""
    if not PEXELS_API_KEY:
        logger.warning("PEXELS_API_KEY not set, skipping video search")
        return None
    try:
        url = f"https://api.pexels.com/videos/search?query={query}&per_page=15&orientation=portrait&size=small"
        headers = {"Authorization": PEXELS_API_KEY}
        timeout = aiohttp.ClientTimeout(total=15)
        async with aiohttp.ClientSession(timeout=timeout) as session:
            async with session.get(url, headers=headers) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    videos = data.get("videos", [])
                    if videos:
                        video = random.choice(videos[:10])
                        # Берём файл SD качества
                        for vf in video.get("video_files", []):
                            if vf.get("quality") == "sd" and vf.get("width", 0) <= 720:
                                return vf.get("link")
                        # Если нет SD, берём первый
                        if video.get("video_files"):
                            return video["video_files"][0].get("link")
    except Exception as e:
        logger.warning("Pexels video search error: %s", e)
    return None


async def search_pexels_photo(query: str) -> Optional[str]:
    """Ищет фото на Pexels, возвращает URL."""
    if not PEXELS_API_KEY:
        return None
    try:
        url = f"https://api.pexels.com/v1/search?query={query}&per_page=15&orientation=landscape"
        headers = {"Authorization": PEXELS_API_KEY}
        timeout = aiohttp.ClientTimeout(total=10)
        async with aiohttp.ClientSession(timeout=timeout) as session:
            async with session.get(url, headers=headers) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    photos = data.get("photos", [])
                    if photos:
                        photo = random.choice(photos[:10])
                        return photo.get("src", {}).get("large2x") or photo.get("src", {}).get("original")
    except Exception as e:
        logger.warning("Pexels photo search error: %s", e)
    return None


