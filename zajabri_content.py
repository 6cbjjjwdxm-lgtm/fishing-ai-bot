# ────────────────────────────── Threads API ──────────────────────────────

async def publish_to_threads(text: str, image_url: Optional[str] = None) -> bool:
    """Публикует пост в Threads через Meta Graph API."""
    if not THREADS_USER_ID or not THREADS_ACCESS_TOKEN:
        logger.warning("Threads API credentials not set — skipping publish")
        return False

    try:
        timeout = aiohttp.ClientTimeout(total=30)
        async with aiohttp.ClientSession(timeout=timeout) as session:
            # Step 1: Create media container
            create_url = f"https://graph.threads.net/{THREADS_USER_ID}/threads"
            payload = {
                "text": text,
                "access_token": THREADS_ACCESS_TOKEN,
            }
            if image_url:
                payload["media_type"] = "IMAGE"
                payload["image_url"] = image_url
            else:
                payload["media_type"] = "TEXT"

            async with session.post(create_url, data=payload) as resp:
                if resp.status != 200:
                    body = await resp.text()
                    logger.error("Threads create container failed: %s %s", resp.status, body)
                    return False
                container = await resp.json()
                container_id = container.get("id")
                if not container_id:
                    logger.error("Threads: no container ID returned")
                    return False

            # Wait for processing
            await asyncio.sleep(5)

            # Step 2: Publish
            publish_url = f"https://graph.threads.net/{THREADS_USER_ID}/threads_publish"
            pub_payload = {
                "creation_id": container_id,
                "access_token": THREADS_ACCESS_TOKEN,
            }
            async with session.post(publish_url, data=pub_payload) as resp:
                if resp.status != 200:
                    body = await resp.text()
                    logger.error("Threads publish failed: %s %s", resp.status, body)
                    return False

        logger.info("Threads post published successfully")
        return True
    except Exception as e:
        logger.error("Threads publish error: %s", e)
        return False


# ────────────────────────────── Instagram Reels API ──────────────────────────────

async def publish_reel_to_instagram(video_url: str, caption: str) -> bool:
    """Публикует Reel в Instagram через Graph API."""
    if not IG_USER_ID or not IG_ACCESS_TOKEN:
        logger.warning("Instagram API credentials not set — skipping reel publish")
        return False

    try:
        timeout = aiohttp.ClientTimeout(total=120)
        async with aiohttp.ClientSession(timeout=timeout) as session:
            # Step 1: Create media container
            create_url = f"https://graph.facebook.com/v18.0/{IG_USER_ID}/media"
            payload = {
                "media_type": "REELS",
                "video_url": video_url,
                "caption": caption,
                "share_to_feed": "true",
                "access_token": IG_ACCESS_TOKEN,
            }
            async with session.post(create_url, data=payload) as resp:
                if resp.status != 200:
                    body = await resp.text()
                    logger.error("IG create container failed: %s %s", resp.status, body)
                    return False
                container = await resp.json()
                container_id = container.get("id")
                if not container_id:
                    logger.error("IG: no container ID")
                    return False

            # Wait for video processing (IG needs time)
            for attempt in range(12):
                await asyncio.sleep(10)
                status_url = f"https://graph.facebook.com/v18.0/{container_id}"
                params = {"fields": "status_code", "access_token": IG_ACCESS_TOKEN}
                async with session.get(status_url, params=params) as resp:
                    if resp.status == 200:
                        data = await resp.json()
                        status = data.get("status_code")
                        if status == "FINISHED":
                            break
                        if status == "ERROR":
                            logger.error("IG container processing error")
                            return False

            # Step 2: Publish
            publish_url = f"https://graph.facebook.com/v18.0/{IG_USER_ID}/media_publish"
            pub_payload = {
                "creation_id": container_id,
                "access_token": IG_ACCESS_TOKEN,
            }
            async with session.post(publish_url, data=pub_payload) as resp:
                if resp.status != 200:
                    body = await resp.text()
                    logger.error("IG publish failed: %s %s", resp.status, body)
                    return False

        logger.info("Instagram Reel published successfully")
        return True
    except Exception as e:
        logger.error("Instagram publish error: %s", e)
        return False


