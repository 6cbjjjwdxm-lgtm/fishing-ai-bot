# ────────────────────────────── Утилиты текста ──────────────────────────────

TG_CAPTION_LIMIT = 1024  # жёсткий лимит Telegram для подписи к фото


def _trim_caption(text: str, limit: int = TG_CAPTION_LIMIT - 10) -> str:
    """Обрезает текст до лимита по последнему пробелу."""
    if len(text) <= limit:
        return text
    trimmed = text[:limit].rsplit(' ', 1)[0].rstrip('.,;:-')
    return trimmed + "..."


def _strip_markdown(text: str) -> str:
    """Убирает Markdown-символы — caption без parse_mode не поддерживает их."""
    return text.replace('**', '').replace('*', '').replace('__', '').replace('_', '').replace('`', '')


# ────────────────────────────── Unsplash ──────────────────────────────

async def get_photo_url(query: str) -> Optional[str]:
    """
    Ищет фото через Unsplash API.
    Возвращает прямую ссылку на изображение или None.
    Если нет ключа Unsplash — возвращает заглушку из Wikimedia.
    """
    if not UNSPLASH_ACCESS_KEY:
        # Запасной вариант — бесплатный Pexels через прямой URL (без API-ключа)
        fallback_queries = [
            "fishing russia",
            "fishing lake",
            "winter fishing ice",
            "fishing rod lake",
        ]
        q = random.choice(fallback_queries)
        # Используем picsum как fallback (случайное изображение)
        seed = abs(hash(query)) % 1000
        return f"https://picsum.photos/seed/{seed}/1280/720"

    try:
        encoded = query.replace(" ", "+")
        url = f"https://api.unsplash.com/photos/random?query={encoded}&orientation=landscape&content_filter=high"
        headers = {"Authorization": f"Client-ID {UNSPLASH_ACCESS_KEY}"}
        timeout = aiohttp.ClientTimeout(total=10)
        async with aiohttp.ClientSession(timeout=timeout) as session:
            async with session.get(url, headers=headers) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    return data.get("urls", {}).get("regular")
    except Exception as e:
        logger.warning("Unsplash error: %s", e)

    seed = abs(hash(query)) % 1000
    return f"https://picsum.photos/seed/{seed}/1280/720"


def _make_image_search_query(topic: str, month: int) -> str:
    """Формирует поисковый запрос для поиска изображения."""
    season = get_season(month)
    keywords_map = {
        "зима": "winter fishing ice",
        "весна": "spring fishing river",
        "лето": "summer fishing lake",
        "осень": "autumn fishing",
    }
    base = keywords_map.get(season, "fishing")

    # Дополнительные ключевые слова из темы
    if any(w in topic.lower() for w in ["щука", "щук"]):
        return f"pike fishing {base}"
    if any(w in topic.lower() for w in ["судак"]):
        return f"pike perch fishing {base}"
    if any(w in topic.lower() for w in ["окунь", "окуня", "окун"]):
        return f"perch fishing {base}"
    if any(w in topic.lower() for w in ["карп", "карпа"]):
        return f"carp fishing {base}"
    if any(w in topic.lower() for w in ["лещ", "лещ"]):
        return f"bream fishing {base}"
    if any(w in topic.lower() for w in ["лёд", "лед", "мормышка", "жерлиц", "зимн"]):
        return "ice fishing winter"
    if any(w in topic.lower() for w in ["спиннинг", "воблер", "джиг"]):
        return f"lure fishing {base}"
    if any(w in topic.lower() for w in ["фидер", "карась", "поплавок"]):
        return f"float fishing {base}"
    return base


# ────────────────────────────── Генерация контент-плана ──────────────────────────────

async def generate_monthly_plan(client: AsyncOpenAI, month: int, year: int) -> List[Dict[str, Any]]:
    """
    Генерирует контент-план на месяц.
    Возвращает список: [{"day": 1, "topic": "...", "post_type": "..."}]
    """
    season = get_season(month)
    ice_status = get_ice_status(month)
    import calendar
    days_in_month = calendar.monthrange(year, month)[1]

    # Берём темы из предзаданного списка
    base_topics = MONTHLY_TOPICS.get(month, [])
    # Если тем меньше чем дней — дублируем
    topics = []
    while len(topics) < days_in_month:
        topics.extend(base_topics)
    topics = topics[:days_in_month]
    # Немного перемешиваем для разнообразия, но сохраняем порядок начала
    random.shuffle(topics[7:])  # первую неделю не трогаем

    post_types = [
        "совет", "обзор места", "гайд", "топ-список",
        "тактика", "обзор снасти", "история", "вопрос-ответ",
    ]

    plan = []
    for day in range(1, days_in_month + 1):
        topic = topics[day - 1]
        ptype = post_types[(day - 1) % len(post_types)]
        plan.append({
            "day": day,
            "date": f"{year}-{month:02d}-{day:02d}",
            "topic": topic,
            "post_type": ptype,
            "season": season,
            "ice_status": ice_status,
        })

    logger.info("Generated content plan for %d/%d: %d posts", month, year, len(plan))
    return plan


# ────────────────────────────── Генерация текста поста ──────────────────────────────

POST_SYSTEM = """
Ты — опытный рыболов и редактор Telegram-канала @dnevnikrib о рыбалке в Московском регионе.

Твоя задача — писать живые, экспертные и полезные посты. Никакой воды — только дельные советы, которые реально помогают на рыбалке. Лёгкий юмор приветствуется.

ПРАВИЛА (соблюдай строго):

1. ДЛИНА: строго до 750 символов включая всё — текст, эмодзи, хэштеги, подпись. Это жёсткий лимит!

2. СТРУКТУРА поста:
   — Начало: цепляющая фраза или факт (без заголовка отдельной строкой)
   — Середина: 2-3 конкретных совета или факта по теме
   — Конец: короткий вопрос или призыв к читателю
   — Подпись: @dnevnikrib — Дневник рыбака
   — Хэштеги: 4-6 штук, среди них ОБЯЗАТЕЛЬНО #дневникрыбака и #клевыйдвиж

3. ЭМОДЗИ: 4-7 штук по тексту, делают пост живым и удобным для чтения

4. СТИЛЬ: опытный рыболов говорит с другом — конкретно, без занудства, с уважением к читателю

5. ОБЯЗАТЕЛЬНО: упоминай Московский регион или конкретные водоёмы Подмосковья

6. НЕ пиши отдельный заголовок — сразу начинай с текста
""".strip()


async def generate_post_text(
    client: AsyncOpenAI,
    topic: str,
    post_type: str,
    month: int,
    season: str,
    ice_status: str,
) -> str:
    """Генерирует текст поста через OpenAI."""
    month_names = {
        1: "январе", 2: "феврале", 3: "марте", 4: "апреле",
        5: "мае", 6: "июне", 7: "июле", 8: "августе",
        9: "сентябре", 10: "октябре", 11: "ноябре", 12: "декабре",
    }
    month_name = month_names.get(month, "")

    prompt = (
        f"Напиши пост для рыболовного Telegram-канала о рыбалке в Московском регионе.\n"
        f"Тема: {topic}\n"
        f"Тип поста: {post_type}\n"
        f"Месяц: {month_name}\n"
        f"Сезон: {season}\n"
        f"Текущая ситуация: {ice_status}\n\n"
        f"Обязательно закончи пост так:\n"
        f"@dnevnikrib \u2014 Дневник рыбака\n"
        f"#дневникрыбака #клевыйдвиж #рыбалка #подмосковье [+ 1-2 тематических хэштега]\n"
        f"(\u042dто точный формат конца поста, не меняй его)"
    )

    try:
        resp = await client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": POST_SYSTEM},
                {"role": "user", "content": prompt},
            ],
            temperature=0.75,
            max_tokens=400,
        )
        text = resp.choices[0].message.content.strip()
        return _trim_caption(text)
    except Exception as e:
        logger.error("Failed to generate post text: %s", e)
        return f"🎣 {topic}\n\nПодробности скоро!\n\n#рыбалка #подмосковье #рыбалкамосква"


# ────────────────────────────── Публикация в канал ──────────────────────────────

async def publish_daily_post(bot, client: AsyncOpenAI) -> bool:
    """
    Основная функция: генерирует и публикует пост на сегодня.
    Вызывается планировщиком каждый день в заданное время.
    """
    now = datetime.datetime.now(datetime.timezone.utc) + datetime.timedelta(hours=3)  # МСК
    today = now.date()
    month = today.month
    year = today.year
    day = today.day

    logger.info("Publishing daily post for %s", today)

    # Получаем тему на сегодня
    topics = MONTHLY_TOPICS.get(month, [])
    if not topics:
        logger.warning("No topics for month %d", month)
        return False

    # Выбираем тему по дню месяца (с оборотом)
    topic_idx = (day - 1) % len(topics)
    topic = topics[topic_idx]

    season = get_season(month)
    ice_status = get_ice_status(month)

    # Определяем тип поста
    post_types = [
        "совет", "обзор места", "гайд", "топ-список",
        "тактика", "обзор снасти", "история", "вопрос-ответ",
    ]
    post_type = post_types[(day - 1) % len(post_types)]

    # Генерируем текст
    text = await generate_post_text(client, topic, post_type, month, season, ice_status)

    # Ищем картинку
    img_query = _make_image_search_query(topic, month)
    photo_url = await get_photo_url(img_query)

    # Публикуем в канал (без Markdown в caption — надёжнее всего)
    safe_text = _trim_caption(_strip_markdown(text))
    try:
        if photo_url:
            await bot.send_photo(
                chat_id=CONTENT_CHANNEL,
                photo=photo_url,
                caption=safe_text,
            )
        else:
            await bot.send_message(
                chat_id=CONTENT_CHANNEL,
                text=safe_text,
            )
        logger.info("Successfully published post: %s", topic[:50])
        return True
    except Exception as e:
        logger.error("Failed to publish post to %s: %s", CONTENT_CHANNEL, e)
        return False


